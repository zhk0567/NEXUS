    private fun addStory(dateStr: String, title: String, content: String, category: String) {
        val date = LocalDate.parse(dateStr, DateTimeFormatter.ISO_LOCAL_DATE)
        // 去除内容首行的前导空格和换行符
        val cleanedContent = content.trimIndent().trimStart().trimStart('\n', '\r')
        val story = Story(
            id = dateStr,
            title = title,
            content = cleanedContent,
            date = date,
            category = category
        )
        stories[dateStr] = story
    }
    
    /**
     * 根据日期获取故事（30天循环）
     * 使用日期模30来确定应该显示哪个故事
     * 基准日期为2025-01-01，确保2025-01-01对应索引0
     */
    fun getStoryByDate(date: LocalDate): Story? {
        // 使用2025-01-01作为基准日期，计算从基准日期到指定日期的天数差，然后模30
        val baseDate = LocalDate.of(2025, 1, 1)
        val daysFromBase = java.time.temporal.ChronoUnit.DAYS.between(baseDate, date)
        val dayIndex = (daysFromBase % 30).toInt()
        // 确保dayIndex在0-29范围内（处理负数情况）
        val storyIndex = if (dayIndex < 0) dayIndex + 30 else dayIndex
        
        // 根据索引获取对应的故事（故事索引从0开始，对应2025-01-01到2025-01-30）
        val targetDate = baseDate.plusDays(storyIndex.toLong())
        val dateStr = targetDate.format(DateTimeFormatter.ISO_LOCAL_DATE)
        
        return stories[dateStr]
    }
    
    /**
     * 获取今天的故事（30天循环）
     */
    fun getTodayStory(): Story? {
        return getStoryByDate(LocalDate.now())
    }
    
    /**
     * 获取所有故事
     */
    fun getAllStories(): List<Story> {
        return stories.values.sortedBy { it.date }
    }
    
    /**
     * 根据类别获取故事
     */
    fun getStoriesByCategory(category: String): List<Story> {
        return stories.values.filter { it.category == category }
            .sortedBy { it.date }
    }
